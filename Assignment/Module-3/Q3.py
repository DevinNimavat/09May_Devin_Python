# Q3.   Differentiate between append () and extend () methods? 

# Append () Method
list=[1,2,3,4,5,6,7]
list.append('8')
print(list)
#output: [1,2,3,4,5,6,7,8]


# Ans: Append Methos is used in add Element after last Element in Current list.
 
# Extend () Methos

list1=[1,2,3,4,5]
list2=['a','b','c','d']
list1.extend(list2)
print(list1)
#output: [1,2,3,4,5,'a','b','c','d']


# Ans: Extend Method is used in add Specified list in Current Element list in last. 